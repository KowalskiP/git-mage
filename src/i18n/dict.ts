/**
 * i18n dictionaries (SPEC §M7). Flat dotted keys; English is the source/
 * fallback. `t()` falls back to the English string, then the raw key, so a
 * missing translation degrades gracefully rather than blanking the UI.
 *
 * Established git terms (commit, branch, checkout, worktree, fetch, pull, push,
 * merge, rebase, stash) stay in English in every locale.
 */
export type Lang = "en" | "ru" | "de" | "fr" | "es" | "zh";

export const LANGS: { code: Lang; label: string }[] = [
  { code: "en", label: "English" },
  { code: "ru", label: "Русский" },
  { code: "de", label: "Deutsch" },
  { code: "fr", label: "Français" },
  { code: "es", label: "Español" },
  { code: "zh", label: "中文" },
];

export function isLang(x: string): x is Lang {
  return LANGS.some((l) => l.code === x);
}

